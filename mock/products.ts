const nextThursday = (weeksAhead = 0) => {
  const d = new Date(); const day = d.getDay(); const diff = (4 - day + 7) % 7 || 7;
  d.setDate(d.getDate() + diff + (weeksAhead * 7)); d.setHours(18, 0, 0, 0); return d.toISOString();
};
const pastDate = (daysAgo: number) => { const d = new Date(); d.setDate(d.getDate() - daysAgo); d.setHours(18, 0, 0, 0); return d.toISOString(); };

